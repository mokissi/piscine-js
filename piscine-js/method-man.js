function words(n) {
    return n.split(" ")
}
function sentence(y) {
    return y.join(" ")
}
function yell(z) {
    return z.toUpperCase()
}
function whisper(x) {
    return `*${x.toLowerCase()}*`
}

function capitalize(s) {
        return s.charAt(0).toUpperCase() + s.slice(1).toLowerCase();
}
    